def _ensure_entity_type_reference(
    session: Session,
    *,
    entity_type: str,
    domain_context: str = "general",
) -> None:
    if not _table_exists(session, "dictionary_entity_types"):
        return

    _ensure_domain_context_reference(session, domain_context)
    normalized = entity_type.strip().upper()
    if not normalized:
        return

    session.execute(
        text(
            """
            INSERT INTO dictionary_entity_types (
                id, display_name, description, domain_context, expected_properties,
                created_by, review_status
            )
            SELECT :id, :display_name, :description, :domain_context, :expected_properties,
                   :created_by, :review_status
            WHERE NOT EXISTS (
                SELECT 1 FROM dictionary_entity_types WHERE id = :id
            )
            """,
        ),
        {
            "id": normalized,
            "display_name": _humanize(normalized),
            "description": "Autogenerated entity type from seed input",
            "domain_context": domain_context,
            "expected_properties": json.dumps({}),
            "created_by": "seed",
            "review_status": "ACTIVE",
        },
    )


