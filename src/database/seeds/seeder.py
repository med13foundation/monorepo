"""
Dictionary Seeder — loads JSON seed files into kernel tables.

Run via: python -m src.database.seeds.seeder
Or from Makefile: make seed-dictionary
"""

from __future__ import annotations

import json
import logging
from datetime import UTC, datetime
from pathlib import Path
from typing import TYPE_CHECKING

from sqlalchemy import inspect, text

from src.type_definitions.dictionary import get_constraint_schema_for_data_type

if TYPE_CHECKING:
    from sqlalchemy.orm import Session

    from src.type_definitions.common import JSONObject

logger = logging.getLogger(__name__)

SEEDS_DIR = Path(__file__).parent

_DATA_TYPE_HINTS: dict[str, tuple[str, str]] = {
    "INTEGER": ("int", "Whole-number value"),
    "FLOAT": ("float", "Decimal numeric value"),
    "STRING": ("str", "Free-form text"),
    "DATE": ("datetime", "Date/time value"),
    "CODED": ("str", "Enumerated coded value"),
    "BOOLEAN": ("bool", "True/False value"),
    "JSON": ("dict", "Structured JSON payload"),
}
_SENSITIVITY_DESCRIPTIONS: dict[str, str] = {
    "PUBLIC": "Data suitable for broad sharing",
    "INTERNAL": "Internal-only research data",
    "PHI": "Sensitive regulated patient data",
}


def _humanize(identifier: str) -> str:
    return " ".join(part.capitalize() for part in identifier.replace("_", " ").split())


def _table_exists(session: Session, table_name: str) -> bool:
    connection = session.connection()
    return inspect(connection).has_table(table_name)


def _ensure_data_type_reference(session: Session, data_type: str) -> None:
    if not _table_exists(session, "dictionary_data_types"):
        return

    normalized = data_type.strip().upper()
    constraint_schema = get_constraint_schema_for_data_type(normalized)
    hint, description = _DATA_TYPE_HINTS.get(
        normalized,
        ("str", "Autogenerated data type from seed input"),
    )
    session.execute(
        text(
            """
            INSERT INTO dictionary_data_types
                (id, display_name, python_type_hint, description, constraint_schema)
            SELECT :id, :display_name, :python_type_hint, :description, :constraint_schema
            WHERE NOT EXISTS (
                SELECT 1 FROM dictionary_data_types WHERE id = :id
            )
            """,
        ),
        {
            "id": normalized,
            "display_name": _humanize(normalized),
            "python_type_hint": hint,
            "description": description,
            "constraint_schema": json.dumps(constraint_schema),
        },
    )
    session.execute(
        text(
            """
            UPDATE dictionary_data_types
            SET constraint_schema = :constraint_schema
            WHERE id = :id
              AND (
                constraint_schema IS NULL
                OR constraint_schema = '{}'::jsonb
              )
            """,
        ),
        {
            "id": normalized,
            "constraint_schema": json.dumps(constraint_schema),
        },
    )


def _ensure_domain_context_reference(session: Session, domain_context: str) -> None:
    if not _table_exists(session, "dictionary_domain_contexts"):
        return

    normalized = domain_context.strip()
    if not normalized:
        return

    session.execute(
        text(
            """
            INSERT INTO dictionary_domain_contexts (id, display_name, description)
            SELECT :id, :display_name, :description
            WHERE NOT EXISTS (
                SELECT 1 FROM dictionary_domain_contexts WHERE id = :id
            )
            """,
        ),
        {
            "id": normalized,
            "display_name": _humanize(normalized),
            "description": "Autogenerated domain context from seed input",
        },
    )


